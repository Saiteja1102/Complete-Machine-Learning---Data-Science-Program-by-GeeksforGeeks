# 1. Arithmetic Operators

# addition
print(12+13)
print(1.2+1)
print(True + False)
print("Sai" + "Teja")
print("------------")

# Subtraction
print(12 - 13)
print(1.2 - 1)
print(True - False)
print("------------")

# Multiplication
print(12 * 13)
print(1.2 * 1)
print(True * False)
print("------------")

# Division
print(12 / 13)
print(1.2 / 1)
print(False / True)
print("------------")

# Floor Division
print(12 // 13)
print(1.2 // 12)
print(12.34 // 13.45)
print("------------")

# Modulo
print(12 % 13)
print(1.2 % 12)
print(12.34 % 13.45)
print(False % True)
print("------------")

# Exponentiation
print(12 ** 13)
print(1.2 ** 12)
print(12.34 ** 13.45)
print(False ** True)
print("------------")

# abs
# negative value will be converted to positive value
print(abs(-35))
print(abs(192))
print(abs(-35.56))
print(abs(192.345))
print("------------")

# round
print(round(123.3456,2))
print(round(-123.3456,1))
print("------------")

# pow
print(pow(12,13))
print(pow(1.2,12))
print(pow(12.34,13.45))
print(pow(False,True))
print("------------")

print("-------------------------------------------")
# Comparision Operators

# ==
print(12 == 12)
print(1.23 == 2.34)
print(True == True)
print("------------")

# !=
print(12 != 12)
print(1.23 != 2.34)
print(True != True)
print("------------")

# >
print(12 > 12)
print(1.23 > 2.34)
print(True > True)
print("------------")

# <
print(12 < 12)
print(1.23 < 2.34)
print(True < True)
print("------------")

# <=
print(12 <= 12)
print(1.23 <= 2.34)
print(True <= True)
print("------------")

# >=
print(12 >= 12)
print(1.23 >= 2.34)
print(True >= True)
print("------------")

print("-------------------------------------------")
# Logical Operators

# and
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("------------")

# or
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print("------------")

# not
print(not True)
print(not False)
print("------------")


